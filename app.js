function fmt(n) { if (!n) return '-'; return Math.round(n).toLocaleString('vi-VN') + 'đ'; }
function fmtShort(n) {
  if (n >= 1000000000) return (n / 1000000000).toFixed(2) + ' tỷ';
  if (n >= 1000000) return (n / 1000000).toFixed(1) + ' tr';
  return fmt(n);
}

